package login.model;

public class UserInfo {
	private String name;
	private String addr;
	private String tel;
	
	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public void setName(String name) {
		this.name = name;
	}	
	
	public String getName() {
		return name;
	}
	
}
